import { ComponentFixture, TestBed } from '@angular/core/testing';
import { of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, convertToParamMap } from '@angular/router';

import { ProcessminingTestModule } from '../../../test.module';
import { ProcessExecutionComponent } from 'app/entities/process-execution/process-execution.component';
import { ProcessExecutionService } from 'app/entities/process-execution/process-execution.service';
import { ProcessExecution } from 'app/shared/model/process-execution.model';

describe('Component Tests', () => {
  describe('ProcessExecution Management Component', () => {
    let comp: ProcessExecutionComponent;
    let fixture: ComponentFixture<ProcessExecutionComponent>;
    let service: ProcessExecutionService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [ProcessminingTestModule],
        declarations: [ProcessExecutionComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: of({
                defaultSort: 'id,asc',
              }),
              queryParamMap: of(
                convertToParamMap({
                  page: '1',
                  size: '1',
                  sort: 'id,desc',
                })
              ),
            },
          },
        ],
      })
        .overrideTemplate(ProcessExecutionComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(ProcessExecutionComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(ProcessExecutionService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new ProcessExecution(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.processExecutions && comp.processExecutions[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new ProcessExecution(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.processExecutions && comp.processExecutions[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should calculate the sort attribute for an id', () => {
      // WHEN
      comp.ngOnInit();
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,desc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // INIT
      comp.ngOnInit();

      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,desc', 'id']);
    });
  });
});
